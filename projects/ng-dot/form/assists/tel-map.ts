import {FormControlAssistConfigure} from '@ng-dot/form';

export const telMap: FormControlAssistConfigure = {
	add: [
		[/\s{2,}/g, ' '],
		
		// 12 1 => +12 1
		[/^(\d+\s)$/, '+$1'],

		// [/^(\+\d+\s)\s+/, '$1'],

		// +82^0 => +82 ^0, korea 
		[/^\+?82([^0])$/, '+82 $1'],

		// except 0
		// +12 12 => +12 1-2 
		[/^(\+\d+\s)([^0\s])(\d)$/, '$1$2-$3'],

		// +12 ^0-1234 => +12 ^0-123-4
		[/^(\+\d+\s)([^0\s]\d?)-(\d{3})(\d)$/, '$1$2-$3-$4'],

		// +12 ^0-123-12345 => +12[^0]-1231-1234
		[/^(\+\d+\s)([^0\s]\d?)-(\d{3})-(\d)(\d{4})$/, '$1$2-$3$4-$5'],

		// +12 1-1-123-1234 => +12 11-123-1234
		[/^(\+\d+\s)([^0\s]\d?)-(\d)-(\d{3})-(\d{4})$/, '$1$2$3-$4-$5'],

		// +12 ^0-1234-12345 => +12[^0]1-2341-2345
		[/^(\+\d+\s)([^0\s]\d?)-(\d)(\d{3})-(\d)(\d{4})$/, '$1$2$3-$4$5-$6'],

		// 1234 => 123-4
		[/^(\+\d+(?:\s\d+)*\s)?(\d{3})(\d)$/, '$1$2-$3'],

		// 123-4-
		[/^(\+\d+(?:\s\d+)*\s)?(\d{3})-(\d)-$/, '$1$2$3-'],

		// 123-12345 => 123-1234-5
		[/^(\+\d+(?:\s\d+)*\s)?(\d{3})-(\d)(\d{4})$/, '$1$2$3-$4'],

		// `02`12-12345 => 02-121-2345, seoul
		[/^(\+\d+(?:\s\d+)*\s)?02(\d\d)-(\d)(\d{4})$/, '$102-$2$3-$4'],

		// 1234-12345 => 123-123-45
		[/^(\+\d+(?:\s\d+)*\s)?(\d{3})(\d)-(\d{3})(\d{2})$/, '$1$2-$3$4-$5'],

		// 123-1234-123 => 123-123-4123
		[/^(\+\d+(?:\s\d+)*\s)?(\d{3})-(\d{3})(\d)-(\d{3})$/, '$1$2-$3-$4$5'],

		// 123-123-12345 => 123-1231-2345
		[/^(\+\d+(?:\s\d+)*\s)?(\d{3})-?(\d{3})-?(\d)(\d{4})$/, '$1$2-$3$4-$5'],

		// `02`-123-12345 => 02-1231-2345, seoul
		[/^(\+\d+(?:\s\d+)*\s)?02-?(\d{3})-?(\d)(\d{4})$/, '$102-$2$3-$4'],

		// 123456789 => 12-3456-7890
		[/^(\d{2})(\d{3,4})(\d{4})$/, '$1-$2-$3'],

		// 123456789 => 123-456-7890
		[/^(\d{3})(\d{3,4})(\d{4})$/, '$1-$2-$3'],

	],
	remove: [
		[/^(\+\d+\s)([^0\s]\d)(\d{3})-(\d{4})$/, '$1$2-$3-$4']
	]
};